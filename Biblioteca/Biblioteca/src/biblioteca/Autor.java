package biblioteca;

/**
 *
 * @author cantarino
 */
public class Autor {
    private String nome;
    private String sobrenome;
    private int id;
    
    public Autor(){
        nome="";
        sobrenome = "";
        id = 0;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getSobrenome() {
        return sobrenome;
    }

    public void setSobrenome(String sobrenome) {
        this.sobrenome = sobrenome;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
    
    
    
}
