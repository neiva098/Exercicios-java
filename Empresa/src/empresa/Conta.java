package empresa;

/**
 *
 * @author neiva
 */
public abstract class Conta extends Pagavel {
    private int dia;
    private int mes;
    public double valor;
    /**
     * @return the dia
     */
    public int getDia() {
        return dia;
    }

    /**
     * @param dia the dia to set
     */
    public void setDia(int dia) {
        this.dia = dia;
    }

    /**
     * @return the mes
     */
    public int getMes() {
        return mes;
    }

    /**
     * @param mes the mes to set
     */
    public void setMes(int mes) {
        this.mes = mes;
    }
    /**
     * @return the valor
     */
    public double getValor() {
        return valor;
    }
    @Override
     public abstract void ValorP();
}
